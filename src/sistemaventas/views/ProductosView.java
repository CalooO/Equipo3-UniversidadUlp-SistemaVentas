    public String formatoPrecio(double x) {
        int aux;
        String numf = null;
        while (x > 1000) {
            aux = (int) (x % 1000);
            x = (x - aux) / 1000;
            if (numf == null) {
                numf = aux + "";
            } else {
                numf = aux + "." + numf;
            }
        }
        if (x != 0) {
            return "$ " + (int) x + "." + numf;
        } else {
            return "$ " + numf;
        }
    }
    
    public double descomponerPrecio(String x){
        String d = null;
        for (int i = 2; i < x.length(); i++) {
            if(x.charAt(i)>=0){
                d = d + x.charAt(i);
            }
        }
        return Double.parseDouble(d);
    }
    
